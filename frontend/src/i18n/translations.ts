function getNestedValue(obj: DeepTranslations, path: string): string | undefined {
  const keys = path.split('.');
  let current: DeepTranslations | string | undefined = obj;

  for (const key of keys) {
    if (typeof current !== 'object' || current === null || !(key in current)) {
      return undefined;
    }
    current = current[key] as DeepTranslations | string;
  }

  return typeof current === 'string' ? current : undefined;
}

export function t(key: string, lang: 'ar' | 'en' = 'ar'): string {
  const result = getNestedValue(translations[lang], key);
  if (result !== undefined) return result;

  if (lang !== 'en') {
    const fallback = getNestedValue(translations['en'], key);
    if (fallback !== undefined) return fallback;
  }

  return key;
}

export default t;
